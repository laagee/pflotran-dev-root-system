subroutine readSSDFromFile(realization,root_property)
  ! 
  ! Reads SSD data from HDF5 for non-overlapping root system.
  ! 
  ! Author: Gautam Bisht
  ! Date: 08/09/14

#if defined(PETSC_HAVE_HDF5)
  use hdf5
#endif

! 64-bit stuff
#ifdef PETSC_USE_64BIT_INDICES
#define HDF_NATIVE_INTEGER H5T_NATIVE_INTEGER
#else
#define HDF_NATIVE_INTEGER H5T_NATIVE_INTEGER
#endif

  use Realization_class
  use Field_module
  use Grid_module
  use Option_module
  use Patch_module
  use Discretization_module
  use Logging_module
  use Input_Aux_module
  use Root_module
  use HDF5_module
  
  implicit none
  
  type(realization_type) :: realization
  type(root_property_type) :: root_property

  type(field_type), pointer :: field
  type(patch_type), pointer :: patch
  type(grid_type), pointer :: grid
  type(option_type), pointer :: option
  type(input_type), pointer :: input
  type(discretization_type), pointer :: discretization
  character(len=MAXSTRINGLENGTH) :: group_name
  character(len=MAXSTRINGLENGTH) :: dataset_name
  character(len=MAXSTRINGLENGTH) :: string 
  PetscInt :: local_id, ghosted_id, natural_id
  PetscReal :: permeability
  PetscBool :: append_realization_id
  PetscInt :: fid = 86
  PetscInt :: status
  PetscInt :: idirection
  PetscInt :: temp_int
  PetscReal :: ratio, scale
  Vec :: natural_vec
  PetscErrorCode :: ierr
  PetscInt :: num_root_prop
  type(root_property_type), pointer :: cur_root_property

  PetscReal, pointer :: vec_p(:)
  PetscReal, pointer :: ssd_loc_p(:)
  PetscReal, pointer :: real_buffer(:)
  PetscInt, pointer :: int_buffer(:)
  PetscInt  :: istart, iend, ii, jj
  PetscInt :: num_cells_local, num_cells_global
  PetscInt :: remainder
  PetscInt, pointer :: indices(:)
  PetscReal, pointer :: nat_vec_p(:)
  PetscReal, pointer :: glb_vec_p(:)
  PetscInt :: rr

  PetscMPIInt       :: hdf5_err
  PetscMPIInt       :: rank_mpi
  PetscInt          :: ndims
#if defined(PETSC_HAVE_HDF5)
  integer(HID_T) :: file_id
  integer(HID_T) :: grp_id
  integer(HID_T) :: prop_id
  integer(HID_T) :: data_set_id
  integer(HID_T) :: file_space_id
  integer(HID_T) :: data_space_id
  integer(HID_T) :: memory_space_id
  integer(HSIZE_T), allocatable :: dims_h5(:), max_dims_h5(:)
  integer(HSIZE_T) :: offset(1), length(1), stride(1), block(1), dims(1)
#endif

  field => realization%field
  patch => realization%patch
  grid => patch%grid
  option => realization%option
  discretization => realization%discretization

  ! Initialize FORTRAN predefined datatypes
  call h5open_f(hdf5_err)

  ! Setup file access property with parallel I/O access
  call h5pcreate_f(H5P_FILE_ACCESS_F, prop_id, hdf5_err)

#ifndef SERIAL_HDF5
  call h5pset_fapl_mpio_f(prop_id,option%mycomm, MPI_INFO_NULL, hdf5_err)
#endif

  ! Open the file collectively
  call h5fopen_f(root_property%ssd_dataset%filename, H5F_ACC_RDONLY_F, &
                 file_id, hdf5_err, prop_id)
  call h5pclose_f(prop_id, hdf5_err)
  
  !
  ! Read "StandardWaterUpdateDensity" dataset
  !

  ! Open dataset
  call h5dopen_f(file_id, "StandardWaterUpdateDensity", data_set_id, hdf5_err)

  ! Get dataset's dataspace
  call h5dget_space_f(data_set_id, data_space_id, hdf5_err)

  ! Get number of dimensions and check
  call h5sget_simple_extent_ndims_f(data_space_id, ndims, hdf5_err)
  if (ndims > 1) &
    call printErrMsg(option,'Dimension of StandardWaterUpdateDensity in ' // &
                     'HDF5 exceeds 1')

  allocate(dims_h5(ndims))
  allocate(max_dims_h5(ndims))
  
  ! Get dimensions of dataset
  call h5sget_simple_extent_dims_f(data_space_id, dims_h5, max_dims_h5, &
                                   hdf5_err)

  num_cells_global = INT(dims_h5(1))
  num_cells_local  = num_cells_global/option%mycommsize
  remainder = num_cells_global - num_cells_local*option%mycommsize
  if (option%myrank < remainder) num_cells_local = num_cells_local + 1

  ! Find istart and iend for hyperslab
  istart = 0
  iend = 0
  call MPI_Exscan(num_cells_local, istart, ONE_INTEGER_MPI, MPIU_INTEGER, &
                  MPI_SUM, option%mycomm, ierr)
  call MPI_Scan(num_cells_local, iend, ONE_INTEGER_MPI, MPIU_INTEGER, &
                MPI_SUM, option%mycomm, ierr)

  ! Determine the length and offset of data to be read by each processor
  length(1) = iend-istart
  offset(1) = istart
  
  ! initialize dataset rank
  rank_mpi = 1
  memory_space_id = -1

  ! Create data space for dataset
  call h5screate_simple_f(rank_mpi, length, memory_space_id, hdf5_err)
  
  ! Select hyperslab
  call h5dget_space_f(data_set_id, data_space_id, hdf5_err)
  call h5sselect_hyperslab_f(data_space_id, H5S_SELECT_SET_F, offset, length, &
                             hdf5_err)

  ! Initialize data buffer
  allocate(real_buffer(length(1)))
  allocate(int_buffer(length(1)))

  ! Create property list
  call h5pcreate_f(H5P_DATASET_XFER_F, prop_id, hdf5_err)
#ifndef SERIAL_HDF5
  call h5pset_dxpl_mpio_f(prop_id, H5FD_MPIO_COLLECTIVE_F, hdf5_err)
#endif
  
  ! Read the dataset collectively
  call h5dread_f(data_set_id, H5T_NATIVE_DOUBLE, real_buffer, &
                 dims_h5, hdf5_err, memory_space_id, data_space_id)

  call h5sclose_f(data_space_id,hdf5_err)
  call h5dclose_f(data_set_id, hdf5_err)

  !
  ! Read "Cell Ids" dataset
  !

  ! Open dataset
  call h5dopen_f(file_id, "Cell Ids", data_set_id, hdf5_err)

  ! Get dataset's dataspace
  call h5dget_space_f(data_set_id, data_space_id, hdf5_err)

  ! initialize dataset rank
  rank_mpi = 1
  memory_space_id = -1

  ! Create data space for dataset
  call h5screate_simple_f(rank_mpi, length, memory_space_id, hdf5_err)
  
  ! Select hyperslab
  call h5dget_space_f(data_set_id, data_space_id, hdf5_err)
  call h5sselect_hyperslab_f(data_space_id, H5S_SELECT_SET_F, offset, length, &
                             hdf5_err)

  ! Create property list
  call h5pcreate_f(H5P_DATASET_XFER_F, prop_id, hdf5_err)
#ifndef SERIAL_HDF5
  call h5pset_dxpl_mpio_f(prop_id, H5FD_MPIO_COLLECTIVE_F, hdf5_err)
#endif
  
  ! Read the dataset collectively
  call h5dread_f(data_set_id, H5T_NATIVE_INTEGER, int_buffer, &
                 dims_h5, hdf5_err, memory_space_id, data_space_id)

  ! Close
  call h5sclose_f(data_space_id,hdf5_err)
  call h5dclose_f(data_set_id, hdf5_err)
  call h5fclose_f(file_id, hdf5_err)
  call h5close_f(hdf5_err)

  call DiscretizationCreateVector(discretization, ONEDOF, natural_vec, &
                                  NATURAL, option)

  ! Converting 1-based index to 0-based
  int_buffer(:) = int_buffer(:) - 1

  call VecSetValues(natural_vec, length(1), int_buffer, real_buffer, INSERT_VALUES, ierr)
  call VecAssemblyBegin(natural_vec, ierr)
  call VecAssemblyEnd(natural_vec, ierr)

  call VecZeroEntries(field%work_loc,ierr);CHKERRQ(ierr)
  call VecZeroEntries(field%work,ierr);CHKERRQ(ierr)

  call DiscretizationNaturalToGlobal(discretization, natural_vec, &
                                     field%work, ONEDOF)
  call DiscretizationGlobalToLocal(discretization, field%work, &
                                   field%work_loc, ONEDOF)

  call VecGetArrayF90(field%work_loc,vec_p,ierr)
  call VecGetArrayF90(field%ssd_loc,ssd_loc_p,ierr)

  do ghosted_id = 1, grid%ngmax
    local_id = grid%nG2L(ghosted_id)
    if (vec_p(ghosted_id) > 0.d0) then
      if (local_id < 1) then
        patch%iroot(0,ghosted_id) = patch%iroot(0,ghosted_id) + 1
        patch%iroot(patch%iroot(0,ghosted_id),ghosted_id) = root_property%id
      endif
      ssd_loc_p((ghosted_id-1)*option%nglb_root_sys + root_property%id) = &
          vec_p(ghosted_id)
    endif
    !endif
  enddo
