! ************************************************************************** !
!> This routine assinges root properties to associated regions in the model
!!
!> @author
!! Gautam Bisht, LBNL
!!
!! date: 09/21/13
! ************************************************************************** !
subroutine assignRootPropToRegions(realization)

  use Realization_class
  use Discretization_module
  use Strata_module
  use Region_module
  use Root_module
  use Option_module
  use Grid_module
  use Field_module
  use Patch_module

  use HDF5_module

  implicit none

  type(realization_type) :: realization
  
  PetscReal, pointer :: kcomp_loc_p(:)
  PetscReal, pointer :: ssd_loc_p(:)
  PetscReal, pointer :: hthreshold_loc_p(:)
  PetscReal, pointer :: vec_p(:)

  PetscInt :: icell, local_id, ghosted_id, natural_id, root_id, iroot
  PetscInt :: istart, iend
  character(len=MAXSTRINGLENGTH) :: group_name
  character(len=MAXSTRINGLENGTH) :: dataset_name
  PetscErrorCode :: ierr
  
  type(option_type), pointer :: option
  type(grid_type), pointer :: grid
  type(discretization_type), pointer :: discretization
  type(field_type), pointer :: field
  type(strata_type), pointer :: strata
  type(patch_type), pointer :: patch  
  type(patch_type), pointer :: cur_patch

  type(root_property_type), pointer :: root_property, null_root_property
  type(region_type), pointer :: region
  PetscBool :: update_ghosted_root_ids
  
  option => realization%option
  discretization => realization%discretization
  patch => realization%patch
  field => realization%field

  option%nglb_root_sys = size(realization%root_property_array)

  cur_patch => realization%patch_list%first
  do
    if (.not.associated(cur_patch)) exit
    if (.not.associated(cur_patch%iroot)) then
      allocate(cur_patch%iroot(0:option%nglb_root_sys,cur_patch%grid%ngmax))
      ! initialize to "unset"
      cur_patch%iroot(:,:) = -999
      cur_patch%iroot(0,:) = 0
    endif
    cur_patch => cur_patch%next
  enddo

  ! if root ids are set based on region, as opposed to being read in
  ! we must communicate the ghosted ids.  This flag toggles this operation.
  update_ghosted_root_ids = PETSC_FALSE
  cur_patch => realization%patch_list%first
  do
    if (.not.associated(cur_patch)) exit
    grid => cur_patch%grid
    strata => cur_patch%strata%first
    do
      if (.not.associated(strata)) exit
      ! Read in cell by cell root ids if they exist
      if (.not.associated(strata%region) .and. strata%active) then
        !call readRootsFromFile(realization,strata%realization_dependent, &
        !                           strata%root_property_filename)
        call printErrMsg(option,'Add readRootsFromFile')
      ! Otherwise, set based on region
      else
        update_ghosted_root_ids = PETSC_TRUE
        region => strata%region
        root_property => strata%root_property
        if (associated(region)) then
          istart = 1
          iend = region%num_cells
        else
          istart = 1
          iend = grid%nlmax
        endif
        do icell=istart, iend
          if (associated(region)) then
            local_id = region%cell_ids(icell)
          else
            local_id = icell
          endif
          ghosted_id = grid%nL2G(local_id)
          if (strata%active) then
            cur_patch%iroot(0,ghosted_id) = cur_patch%iroot(0,ghosted_id) + 1
            cur_patch%iroot(cur_patch%iroot(0,ghosted_id),ghosted_id) = root_property%id
          else
            ! if not active, set root id to zero
            cur_patch%iroot(:,ghosted_id) = 0
          endif
        enddo
      endif
      strata => strata%next
    enddo
    cur_patch => cur_patch%next
  enddo

  ! Allocate memory for ssd_loc
  call VecCreate(option%mycomm, field%ssd_loc, ierr)
  call VecSetSizes(field%ssd_loc, grid%ngmax*option%nglb_root_sys, &
                  PETSC_DECIDE, ierr)
  call VecSetBlockSize(field%ssd_loc, option%nglb_root_sys, ierr)
  call VecSetFromOptions(field%ssd_loc, ierr)

  ! set cell by cell root properties
  ! create null root property for inactive cells
  null_root_property => RootPropertyCreate()
  cur_patch => realization%patch_list%first
  do
    if (.not.associated(cur_patch)) exit
    grid => cur_patch%grid
    call VecGetArrayF90(field%ssd_loc,ssd_loc_p,ierr)
    call VecGetArrayF90(field%kcomp_loc,kcomp_loc_p,ierr)
    call VecGetArrayF90(field%hthreshold_loc,hthreshold_loc_p,ierr)

    do local_id = 1, grid%nlmax
      ghosted_id = grid%nL2G(local_id)
      do iroot = 1,cur_patch%iroot(0,ghosted_id)
        root_id = cur_patch%iroot(iroot, ghosted_id)
        if (root_id == 0) then ! accommodate inactive cells
          root_property => null_root_property
        else if (root_id > 0 .and. &
                 root_id <= &
                 size(realization%root_property_array)) then
          root_property => &
            realization%root_property_array(root_id)%ptr
          if (.not.associated(root_property)) then
            write(dataset_name,*) root_id
            option%io_buffer = 'No root property for root id ' // &
                               trim(adjustl(dataset_name)) &
                               //  ' defined in input file.'
            call printErrMsgByRank(option)
          endif
        else if (root_id < -998) then 
          write(dataset_name,*) grid%nG2A(ghosted_id)
          option%io_buffer = 'Uninitialized root id in patch at cell ' // &
                             trim(adjustl(dataset_name))
          call printErrMsgByRank(option)
        else if (root_id > size(realization%root_property_array)) then
          write(option%io_buffer,*) root_id
          option%io_buffer = 'Unmatched root id in patch:' // &
            adjustl(trim(option%io_buffer))
          call printErrMsgByRank(option)
        else
          option%io_buffer = 'Something messed up with root ids. ' // &
            ' Possibly root ids not assigned to all grid cells. ' // &
            ' Contact Gautam!'
          call printErrMsgByRank(option)
        endif
        kcomp_loc_p(ghosted_id) = root_property%Kcomp
        hthreshold_loc_p(ghosted_id) = root_property%Hthreshold

        ssd_loc_p((ghosted_id-1)*option%nglb_root_sys + root_property%id) = &
          root_property%ssd
      enddo
    enddo

    call VecRestoreArrayF90(field%ssd_loc,ssd_loc_p,ierr)
    call VecRestoreArrayF90(field%kcomp_loc,kcomp_loc_p,ierr)
    call VecRestoreArrayF90(field%hthreshold_loc,hthreshold_loc_p,ierr)

    ! read in any user-defined property fields
    do root_id = 1, size(realization%root_property_array)
      root_property => &
             realization%root_property_array(root_id)%ptr
      if (associated(root_property)) then
        if (associated(root_property%ssd_dataset)) then
          call readSSDFromFile(realization,root_property)
        endif
      endif
    enddo

    cur_patch => cur_patch%next
  enddo
  call RootPropertyDestroy(null_root_property)
  nullify(null_root_property)

  ! update ghosted values
  call DiscretizationLocalToLocal(discretization,field%kcomp_loc, &
                                   field%kcomp_loc,ONEDOF)
  call DiscretizationLocalToLocal(discretization,field%hthreshold_loc, &
                                   field%hthreshold_loc,ONEDOF)


  ! Create the PETSc vector to save transpiration sink at root-system level
  ! for output
  call VecCreate(option%mycomm, field%trootsys_loc, ierr)
  call VecSetSizes(field%trootsys_loc, grid%ngmax*option%nglb_root_sys, &
                  PETSC_DECIDE, ierr)
  call VecSetBlockSize(field%trootsys_loc, option%nglb_root_sys, ierr)
  call VecSetFromOptions(field%trootsys_loc, ierr)

end subroutine assignRootPropToRegions

